# 이 파일에는 변수, 데이터 유형, 기본 연산 및 인쇄 문과 관련된 초보자 Python 연습이 포함되어 있습니다.

# 변수 선언
x = 5  # 정수형 변수
y = 3.2  # 부동 소수점형 변수
name = "안녕하세요"  # 문자열형 변수

# 기본 데이터 유형
print(type(x))  # <class 'int'>와 같은 유형 출력
print(type(y))  # <class 'float'>와 같은 유형 출력
print(type(name))  # <class 'str'>와 같은 유형 출력

# 기본 연산
sum_result = x + y  # 덧셈
subtract_result = x - y  # 뺄셈
multiply_result = x * y  # 곱셈
divide_result = x / y  # 나눗셈

# 결과 출력
print(f"x + y = {sum_result}")  # x와 y의 합
print(f"x - y = {subtract_result}")  # x에서 y를 뺀 값
print(f"x * y = {multiply_result}")  # x와 y의 곱
print(f"x / y = {divide_result}")  # x를 y로 나눈 값